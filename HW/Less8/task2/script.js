/*Задание 2
Описание задачи: Имеется массив [1, 2, 3, 4, 5, 6, 7]. Необходимо удалить
элементы 4 и 5, чтобы получить массив [1, 2, 3, 6, 7].
*/

const arr1 = [1, 2, 3, 4, 5, 6, 7];
const arr2 = [];
for (let i in arr1) {
	if (!(arr1[i] == 4) && !(arr1[i] == 5)) {
		arr2.push(arr1[i]);
	}
}
console.log(arr2);
